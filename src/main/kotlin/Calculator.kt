class Calculator {  // Kelas Calculator untuk melakukan operasi perhitungan sederhana
    fun add(first: Int, second: Int): Int {  // Fungsi untuk menambahkan dua angka dan mengembalikan hasilnya
        return first + second
    }

    fun sub(first: Int, second: Int): Int {  // Fungsi untuk mengurangkan dua angka dan mengembalikan hasilnya
        return first - second
    }
}
